#!/usr/bin/env python3
"""Generate Russian datasets for 9 icons that have no ru files."""
from pathlib import Path
import csv

icons_dir = Path("icons")

def write_csv(path, rows):
    with open(path, "w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(["text", "label"])
        w.writerows(rows)

def save(icon, train, valid, test):
    d = icons_dir / icon
    write_csv(d / "train_ru.csv", [(t, icon) for t in train])
    write_csv(d / "valid_ru.csv", [(t, icon) for t in valid])
    write_csv(d / "test_ru.csv",  [(t, icon) for t in test])
    print(f"  {icon}: {len(train)} train / {len(valid)} valid / {len(test)} test")

